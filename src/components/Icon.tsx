const getIconProps = (title, decorative) => {
  return decorative
    ? {
        'aria-hidden': true,
        focusable: false,
      }
    : {
        'aria-label': title,
        role: 'img',
      }
}

export default function Icon({
  decorative = true,
  fill = 'currentColor',
  icon,
  label = '',
  size,
}: {
  icon: keyof typeof icons
  size: 'small' | 'medium' | 'large'
  fill?: string
  decorative?: boolean
  label?: string
}): JSX.Element {
  if (!icons[icon]) throw new Error('Unknown icon.')

  const title = label || icon.replace(/-/g, ' ')

  return (
    <svg
      className={classNames('Icon', size && size)}
      fill={fill}
      viewBox='0 0 32 32'
      xmlns='http://www.w3.org/2000/svg'
      {...getIconProps(title, decorative)}
    >
      <title>{title}</title>
      {icons[icon]}
    </svg>
  )
}
